package com.pd.demojsoup;

class Chapter {

    private String imgChapter;
    private String nameChapter;
    private String volChapter;
    private String nameManga;

    public String getImgChapter() {
        return imgChapter;
    }

    public void setImgChapter(String imgChapter) {
        this.imgChapter = imgChapter;
    }

    public String getNameChapter() {
        return nameChapter;
    }

    public void setNameChapter(String nameChapter) {
        this.nameChapter = nameChapter;
    }

    public String getVolChapter() {
        return volChapter;
    }

    public void setVolChapter(String volChapter) {
        this.volChapter = volChapter;
    }

    public String getNameManga() {
        return nameManga;
    }

    public void setNameManga(String nameManga) {
        this.nameManga = nameManga;
    }
}
